package org.ssa.ironyard.restful.model;

public class TrafficStop
{
    String date_of_stop;
    String time_of_stop;
    Double latitude;
    Double longitude;
    String location;
    String vehicle_type;
    String make;
    String model;
    String year;
    String color;
    
    public String getDate_of_stop()
    {
        return date_of_stop;
    }
    public void setDate_of_stop(String date_of_stop)
    {
        this.date_of_stop = date_of_stop;
    }
    public String getTime_of_stop()
    {
        return time_of_stop;
    }
    public void setTime_of_stop(String time_of_stop)
    {
        this.time_of_stop = time_of_stop;
    }
    public Double getLatitude()
    {
        return latitude;
    }
    public void setLatitude(Double latitude)
    {
        this.latitude = latitude;
    }
    public Double getLongitude()
    {
        return longitude;
    }
    public void setLongitude(Double longitude)
    {
        this.longitude = longitude;
    }
    public String getLocation()
    {
        return location;
    }
    public void setLocation(String location)
    {
        this.location = location;
    }
    public String getVehicle_type()
    {
        return vehicle_type;
    }
    public void setVehicle_type(String vehicle_type)
    {
        this.vehicle_type = vehicle_type;
    }
    public String getMake()
    {
        return make;
    }
    public void setMake(String make)
    {
        this.make = make;
    }
    public String getModel()
    {
        return model;
    }
    public void setModel(String model)
    {
        this.model = model;
    }
    public String getYear()
    {
        return year;
    }
    public void setYear(String year)
    {
        this.year = year;
    }
    public String getColor()
    {
        return color;
    }
    public void setColor(String color)
    {
        this.color = color;
    }

    

}
